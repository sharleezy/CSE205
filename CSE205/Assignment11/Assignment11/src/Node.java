public class Node {
	private int x;
	private int y;
	
	//constructor
	public Node(int x, int y) {
		this.x = x;
		this.y = y;
	}
	
	//getter method to return x value
	public int getX() {
		return x;
	}
	
	//getter method to return y value
	public int getY() {
		return y;
	}
}
